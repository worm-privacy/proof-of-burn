#  (sigma) function
def sigma(x):
    x2 = x * x
    x4 = x2 * x2
    return x4 * x


# Add round constants
def ark(t, state, c, round_idx):
    return [state[i] + c[round_idx * t + i] for i in range(t)]


# Mix with MDS matrix
def mix(t, state, m):
    out = []
    for i in range(t):
        acc = Field(0)
        for j in range(t):
            acc = acc + m[j][i] * state[j]
        out.append(acc)
    return out


# Poseidon hash function
def poseidon(inp):
    cnt = len(inp)
    state = [Field(0)] + inp
    rp = ROUNDS_P[cnt - 1]
    c = POSEIDON_C[cnt - 1]
    m = POSEIDON_M[cnt - 1]
    for r in range(ROUNDS_F + rp):
        state = ark(cnt + 1, state, c, r)

        if r < ROUNDS_F // 2 or r >= ROUNDS_F // 2 + rp:
            state = [sigma(x) for x in state]
        else:
            state[0] = sigma(state[0])

        state = mix(cnt + 1, state, m)

    return state[0]


def poseidon1(a):
    return poseidon([a])


def poseidon2(a, b):
    return poseidon([a, b])


def poseidon3(a, b, c):
    return poseidon([a, b, c])


